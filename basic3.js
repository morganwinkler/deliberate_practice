// Write a while loop to print the numbers 1 through 10.

let i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

// Write a while loop that prints the word "hello" 5 times.
let hello = 0;
while (hello < 5) {
  console.log("hello");
  hello++;
}

// Write a while loop that prints the numbers 0 through 100, increasing by 5 each time
let num = 0;
while (num <= 100) {
  console.log(num);
  num = num + 5;
}
